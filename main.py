# === 3. –í–´–ë–û–† –ö–û–ù–ö–†–ï–¢–ù–û–ì–û –ú–ê–†–ö–ï–¢–ê ===
@dp.callback_query_handler(lambda c: c.data.startswith("market:"))
async def process_market_callback(callback_query: types.CallbackQuery):
    user_id = callback_query.from_user.id
    data = pending_reports.get(user_id)

    # –ï—Å–ª–∏ –æ—Ç—á—ë—Ç–∞ –¥–ª—è —ç—Ç–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–µ—Ç ‚Äì –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º
    if not data or data.get("step") != "choose_market":
        await callback_query.answer("–ù–µ—Ç –∞–∫—Ç–∏–≤–Ω–æ–≥–æ –æ—Ç—á—ë—Ç–∞. –û—Ç–ø—Ä–∞–≤—å —Ñ–æ—Ç–æ –µ—â—ë —Ä–∞–∑.")
        return

    market_name = callback_query.data.split(":", 1)[1]
    data["market"] = market_name
    data["step"] = "fill_template"

    # –ù–∞ –≤—Å—è–∫–∏–π —Å–ª—É—á–∞–π –¥–æ–±–∞–≤–∏–º –º–∞—Ä–∫–µ—Ç –≤ —Å–ª–æ–≤–∞—Ä—å –æ—Ç—á—ë—Ç–æ–≤, –µ—Å–ª–∏ –µ–≥–æ —Ç–∞–º –Ω–µ—Ç
    if market_name not in daily_reports:
        daily_reports[market_name] = False

    template_text = (
        f"#–ú–∞–≥–∞–∑–∏–Ω: {market_name}\n"
        f"–û—Å—Ç–∞—Ç–∫–∏: \n"
        f"–•–ª–µ–±: \n"
        f"–õ–µ–ø–µ—à–∫–∏: \n"
        f"–ü–∞—Ç—ã—Ä: \n"
        f"–ê—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç: "
    )

    await callback_query.message.reply(
        "–¢–µ–ø–µ—Ä—å –∑–∞–ø–æ–ª–Ω–∏ —à–∞–±–ª–æ–Ω, —É–∫–∞–∑–∞–≤ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —à—Ç—É–∫ –ø–æ –∫–∞–∂–¥–æ–º—É –ø—É–Ω–∫—Ç—É "
        "–∏ –æ—Ç–ø—Ä–∞–≤—å –°–õ–ï–î–£–Æ–©–ò–ú —Å–æ–æ–±—â–µ–Ω–∏–µ–º:",
    )
    await callback_query.message.reply(f"<code>{template_text}</code>")

    await callback_query.answer("–ú–∞—Ä–∫–µ—Ç –≤—ã–±—Ä–∞–Ω!")


# === 4. –ü–û–õ–£–ß–ê–ï–ú –ó–ê–ü–û–õ–ù–ï–ù–ù–´–ô –®–ê–ë–õ–û–ù ===
@dp.message_handler(content_types=types.ContentType.TEXT)
async def handle_template_text(message: types.Message):
    user_id = message.from_user.id
    data = pending_reports.get(user_id)

    # –ï—Å–ª–∏ –Ω–µ—Ç –∞–∫—Ç–∏–≤–Ω–æ–≥–æ —à–∞–≥–∞ "–∑–∞–ø–æ–ª–Ω–∏ —à–∞–±–ª–æ–Ω" ‚Äì –ø—Ä–æ—Å—Ç–æ –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç
    if not data or data.get("step") != "fill_template":
        return

    # –ú–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –ø—Ä–æ—Å—Ç—É—é –ø—Ä–æ–≤–µ—Ä–∫—É, —á—Ç–æ —Ç–µ–∫—Å—Ç –ø–æ—Ö–æ–∂ –Ω–∞ –Ω—É–∂–Ω—ã–π —à–∞–±–ª–æ–Ω
    # –Ω–æ –∂—ë—Å—Ç–∫–æ –Ω–µ –±—É–¥–µ–º –≤–∞–ª–∏–¥–∏—Ä–æ–≤–∞—Ç—å, —á—Ç–æ–±—ã –Ω–µ –º–µ—à–∞—Ç—å –ª—é–¥—è–º.
    market_name = data.get("market")
    report_date = data.get("date")
    today = datetime.now().date()

    # –ï—Å–ª–∏ –¥–∞—Ç–∞ —Å–º–µ–Ω–∏–ª–∞—Å—å ‚Äì —Å—á–∏—Ç–∞–µ–º, —á—Ç–æ –æ—Ç—á—ë—Ç –ø—Ä–æ—Å—Ä–æ—á–µ–Ω
    if report_date != today:
        pending_reports.pop(user_id, None)
        await message.reply("–î–µ–Ω—å —É–∂–µ —Å–º–µ–Ω–∏–ª—Å—è, –æ—Ç–ø—Ä–∞–≤—å, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, –Ω–æ–≤—ã–π –æ—Ç—á—ë—Ç —Å —Ñ–æ—Ç–æ.")
        return

