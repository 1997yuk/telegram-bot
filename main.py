    daily_reports[market_name] = True

    # –ú–æ–∂–Ω–æ —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å —Å–∞–º —Ç–µ–∫—Å—Ç –æ—Ç—á—ë—Ç–∞ –≤ –ª–æ–≥ / –ë–î (–∑–¥–µ—Å—å –ø—Ä–æ—Å—Ç–æ –ª–æ–≥–∏—Ä—É–µ–º)
    logging.info(f"–û—Ç—á—ë—Ç {market_name} –æ—Ç {user_id}: {message.text}")

    # –û—á–∏—â–∞–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    pending_reports.pop(user_id, None)

    await message.reply(f"–û—Ç—á—ë—Ç –¥–ª—è <b>{market_name}</b> —Å–æ—Ö—Ä–∞–Ω—ë–Ω ‚úÖ –°–ø–∞—Å–∏–±–æ!")


